# Exercício 1.2 Armazene dois números em variáveis e imprima a soma, subtração, multiplicação e divisão deles.

n1 = int(input("Digite um numéro: "))
n2 = int(input("Digite outro numéro: "))

print(f"{n1} + {n2} = {n1 + n2}")
print(f"{n1} - {n2} = {n1 - n2}")
print(f"{n1} * {n2} = {n1 * n2}")
print(f"{n1} / {n2} = {n1 / n2}")

